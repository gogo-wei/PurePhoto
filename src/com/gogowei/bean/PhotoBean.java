package com.gogowei.bean;

public class PhotoBean {

	private String path;
	private int size;
	private String latModifyTime;
	
	public String getPath() {
		return path;
	}
	public void setPath(String path) {
		this.path = path;
	}
	public int getSize() {
		return size;
	}
	public void setSize(int size) {
		this.size = size;
	}
	public String getLastModifyTime() {
		return latModifyTime;
	}
	public void setLastModifyTime(String latModifyTime) {
		this.latModifyTime = latModifyTime;
	}

}
